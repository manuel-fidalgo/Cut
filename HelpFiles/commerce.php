
El comercio será capaz de ofrecer la siguiente información al usuario:
 - Listado de los servicios que ofrece.
 - Precio de los servicios.
 - Duración.
 - Horario de apertura.
 - Capacidad.
 - Días de cierre.

A su vez, de una forma sencilla el comercio es capaz de modificar toda esa información en caso de que se produjeran cambios y fuera necesario cambiar el precio de un servicio, su duración, informar de cambios respecto a los días en los que permanecerá cerrado, etc.

Los comercios tienen la posibilidad de añadir servicios. Para ello solo hay que indicar el nombre del servicio, su duración, el precio y el número de personas que pueden ser atendidas a la vez.
También podrán borrar servicios si lo desean, solo deberán indicar el nombre del servicio que deseen eliminar.

Cuando se añade un nuevo servicio saldrá un mensaje indicando si el proceso se ha llevado a cabo correctamente o si ha habido algún error.
 