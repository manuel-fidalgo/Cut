Proceso común a clientes y peluquerías.
Es necesario introducir el nombre con el que el usuario fue registrado y su contraseña.
En caso de que la contraseña introducida sea inválidad habrá que repetir el proceso.
Dependiendo del usuario que se haya introducido se accederá a dos vistas diferentes (con sus respectivas funciones) según sea un cliente o una peluquería.

