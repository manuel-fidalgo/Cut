Una vez accedido al usuario veremos:

- En caso del cliente: un apartado para buscar peluquerías y las reservas actuales (aparece el nombre de la peluquería en la que se realizó la reserva, la fecha, la hora y el tipo de servicio).
También puede escribir un comentario sobre la peluquería y valorarla usando una puntuación cuyo máximo es 5.

- En caso de la peluquería: aparecen las reservas realizadas por los clientes (como en el caso anterior, con la misma información) y, además, la posibilidad de crear un nuevo servicio, borrar un servicio, gestionar los horarios de la peluquería, etc.