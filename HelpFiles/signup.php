En la aplicación se podrán registrar tanto clientes como establecimientos.
Para ello solo deberán indicar:
 - Nombre
 - Correo electrónico
 - Contraseña
 - Tipo de usuario: cliente o peluquería

En caso de que el usuario que se está registrando sea un cliente, el proceso está completado y recibirá una notificación en caso de que haya habido algún error o se haya guardado correctamente.

Si el usuario selecciona peluquería se añaden los siguientes puntos:
 - Código postal
 - Dirección
 - Ciudad
 - Nombre del comercio
 - Descripción

Aquí habrá terminado el registro en caso de ser un establecimiento; al igual que en el caso del cliente, se recibe una notificación.